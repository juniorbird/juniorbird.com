				<h2>Travelogues</h2>

				<h3>Tanzania Safari</h3>
				<p>An amazing place to enjoy a safari &mdash; a beautiful and pleasant country. After <a href="http://juniorbird.com/archive/004129.html">a long plane ride</a> (I recommend Turkish Air), you can enjoy the <a href="http://juniorbird.com/archive/004127.html">remote, chimp-filled Mahale Mountains</a>; quaint <a href="http://juniorbird.com/archive/004126.html">Arusha</a>; the  <a href="http://juniorbird.com/archive/004154.html">amazing Ruaha</a> &amp; <a href="http://juniorbird.com/archive/004153.html">the vast Selous</a> game preserves; and <a href="http://juniorbird.com/archive/004152.html">the jewel-like Zanzibar</a>. Here's my <a href="http://juniorbird.com/archive/004151.html">overall advice on a Tanzania safari</a>.</p>

				<h3>France</h3>
				<p>Ahh, France: we started out with <a href="http://juniorbird.com/archive/004056.html">several</a> <a href="http://juniorbird.com/archive/004054.html">days in</a> <a href="http://juniorbird.com/archive/004053.html">Paris</a>; then south to <a href="http://juniorbird.com/archive/004052.html">the Loire and Burgundy</a>, where we saw <a href="http://juniorbird.com/archive/004045.html">castles of all types</a>; and capped it off <a href="http://juniorbird.com/archive/004051.html">on a Mediterranean island</a>. Had a <a href="http://juniorbird.com/archive/004050.html">great flight there on Air France and a miserable one back on Delta</a>.</p>

				<h3>Honeymoon to New Zealand and Fiji</h3>
				<p>We started in <a href="http://juniorbird.com/archive/003834.html">Christchurch in the South Island</a>, then <a href="http://juniorbird.com/archive/003835.html">got away to treehouses in Kaikoura</a>; we <a href="http://juniorbird.com/archive/003837.html">crisscrossed the island some</a>, seeing <a href="http://juniorbird.com/archive/003838.html">glaciers and mountain vistas</a>, enjoying <a href="http://juniorbird.com/archive/003839.html">jetboats and hikes on remote mountains</a> and <a href="http://juniorbird.com/archive/003840.html">sailing remote fjords</a>, before <a href="http://juniorbird.com/archive/003841.html">relaxing in Queenstown</a>. Then it was <a href="http://juniorbird.com/archive/003842.html">off to a little island by Fiji</a> where we enjoyed <a href="http://juniorbird.com/archive/003843.html">quiet relaxation</a> and <a href="http://juniorbird.com/archive/003844.html">enjoyed being married</a> while <a href="http://juniorbird.com/archive/003845.html">being pampered and entertained</a>.</p>

				<h3>Hawaii</h3>
				<p>It is the stereotypical US getaway... and for good reason! <a href="http://juniorbird.com/archive/003690.html">Even just arriving there</a> makes you <a href="http://juniorbird.com/archive/003691.html">loosen up</a> and <a href="http://juniorbird.com/archive/003692.html">relax</a>! We went to <a href="http://juniorbird.com/archive/003693.html">amazing resort on Kauai</a>, where we indulged in <a href="http://juniorbird.com/archive/003694.html">the beach and a luau</a>.
				</p>

				<h3>Southeast Asia</h3>
				<p>Maybe my favorite trip ever, my month in Southeast Asia alone was eye-opening and relaxing! After <a href="http://juniorbird.com/archive/002677.html">a long flight</a>, I found myself in <a href="http://juniorbird.com/archive/002678.html">bustling Bangkok</a>, where I <a href="http://juniorbird.com/archive/002679.html">had my first Thai massage and traveled all around by boat and tuk-tuk</a>, seeing <a href="http://juniorbird.com/archive/002680.html">palaces and eating authentic Thai food</a>. Then it was <a href="http://juniorbird.com/archive/002682.html">off to Vietnam</a>, starting <a href="http://juniorbird.com/archive/002683.html">in ancient Hanoi</a>, where I <a href="http://juniorbird.com/archive/002689.html">enjoyed the food and saw the museums</a>, and <a href="http://juniorbird.com/archive/002690.html">traveled to ancient ruins</a>. <a href="http://juniorbird.com/archive/002691.html">Imperial capital Hue</a> was next, a <a href="http://juniorbird.com/archive/002693.html">parade of palaces and tombs</a>. Saigon was my last stop, <a href="http://juniorbird.com/archive/002696.html">all urban bustle</a>. I <a href="http://juniorbird.com/archive/002703.html">traveled up the Mekong</a> to <a href="http://juniorbird.com/archive/002695.html"> Phnom Penh in Cambodia, with its memories of the Killing Fields</a>. A quick bus ride had me at <a href="http://juniorbird.com/archive/002697.html">ancient wonder of the world</a> <a href="http://juniorbird.com/archive/002698.html">Angkor Wat</a> and some of the best food of the trip. Exhausted, I relaxed for my last few days in <a href="http://juniorbird.com/archive/002699.html">Chiang Mai in Northern Thailand</a>. Don't want to wade through all the entries? Try <a href="http://juniorbird.com/archive/002715.html">the best photos of the trip</a>.</p>

				<h3>PRIME Chile/Brazil</h3>
				<p>One of the greatest parts of getting my <a href="http://www.marshall.usc.edu/">MBA at USC</a> was <a href="http://www.marshall.usc.edu/mba/curriculum/prime">the PRIME program, where we traveled to other countries</a> and carried out little consulting projects for companies there. I <a href="http://juniorbird.com/archive/001503.html">went to Chile</a> and Brazil. We <a href="http://juniorbird.com/archive/001506.html">started in Santiago</a> and <a href="http://juniorbird.com/archive/001693.html">walked about the city</a>, saw the <a href="http://juniorbird.com/archive/001507.html">Concha y Toro winery</a>, then jetted to <a href="http://juniorbird.com/archive/001509.html">Sao Paulo in Brazil</a>, where I <a href="http://juniorbird.com/archive/001706.html">enjoyed the town</a> and <a href="http://juniorbird.com/archive/001707.html">its' many churches</a>, discovered the Caipirinha, <a href="http://juniorbird.com/archive/001510.html">Samba, and Acai</a>, and even <a href="http://juniorbird.com/archive/001716.html">had Churrasco</a>. Our <a href="http://juniorbird.com/archive/001508.html">consulting presentation went well.</a> Then it was off to Rio, where I <a href="http://juniorbird.com/archive/001511.html">went all over town with friends</a>, enjoyed <a href="http://juniorbird.com/archive/001717.html">the view of the city from above</a>, visited <a href="http://juniorbird.com/archive/001728.html">that Christ on a hill</a>, <a href="http://juniorbird.com/archive/001513.html">an old fort</a> (pictures <a href="http://juniorbird.com/archive/001738.html">here</a>), saw<a href="http://juniorbird.com/archive/001740.html"> many more cool churches</a>, and, of course <a href="http://juniorbird.com/archive/001747.html">enjoyed the beaches</a>. I <a href="http://juniorbird.com/archive/001520.html">loved them both but Brazil was the winner.</a>
				</p>

