		<h2>Cured!</h2>
	
		<p>I'm officially cured; praise be, my legs are whole again!</p>

		<p>Well, not that whole. I'm supposed to start out by running a quarter mile, then increase that slowly. A quarter mile &mdash; that's barely long enough to bother running! The doc's mostly worried that I still have some soreness in the bones right above my ankles.</p>
		
		<p>My foot pain has almost totally disappeared, thanks to my orthotics:<br />
		<img src="/images/kerlanjobe/boththotics.jpg" alt="two orthotics next to each other, white plastic"><br />
		Look just like a pair o'lungs, don't they?<br />
		From the bottom you can see how they're molded to my feet:<br />
		<img src="/images/kerlanjobe/underthotics.jpg" alt="the bottom undulations of my orthotics"><br />
		That shaping lets them support my foot where it needs it most, at the arch. They're also a bit bucket-shaped:<br />
		<img src="/images/kerlanjobe/buckethotic.jpg" alt="the concave inner surface of the orthotics"><br />
		They cup my heel and the area above my arch, preventing my heel from pronating along with my arch. Let me tell you, these things are like a continuous foot massage! I go to bed each night wishing I could wear my shoes longer. I'm a fan and a convert.</p>
		
		<p>What I'm not a fan of is how long I had to wait to see the doctor to get them. My podiatrist, Dr. Kwong, was almost an hour late for our appointment; fortunately I was coming there from physical therapy, so we were able to devise another 45 minutes of exercise and I was spared the waiting room. I was not so fortunate for my follow-up appointment for my shin splints with Dr. Glousman. He was running more than two hours behind, and, as much as I do have fun games installed on my cell phone, I was reduced to reading Jet magazine in his waiting room (not that there's anything wrong with Jet, just, even were I black, the demographic would still be about 30 years older than me).</p>
		
		<p>I guess the lesson I learned from this is to always call ahead before you leave for a doctor's appointment to make sure your doc is running on schedule (although I wonder if they would've admitted how far behind he was).</p>
		
		<p>But how do you get two whole hours behind and not take 15 minutes to consider what you should do about the rest of your appointments that day? I would assume that the scheduling person would say something once you got kinda out of control, say at 45 minutes behind. Then it would be time to start trying to reschedule some appointments so that you could make others. I know I saw other people walking out because Glousman was so late, and I know that I would certainly think twice before seeing him in the future &mdash; I think I got good care, I just don't know what standard of care would be worth two hours of my life, given that it was a followup appointment for a non-serious problem that, heck, I could feel had been cured.  </p>
		
		<p>So, in sum:<br />
		<ul><li>I love the physical therapists at Kerlan-Jobe</li><li>I love Dr. Fleckner, their orthotics guy, because he makes my feets feel gooood</li><li>I'm not so much a fan of their regular docs, because you can sure have to wait to see them. But when you do see them, they know what they're doing and take good care of you</li></ul></p>

